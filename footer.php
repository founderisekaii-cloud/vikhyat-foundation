<script>
    function showToast(message, type = 'success') {
        const container = document.getElementById('toast-container');
        const toast = document.createElement('div');

        // Dynamic Island Style: Black background, pill shape
        // Success: Green Icon | Error: Red Icon
        const baseClasses = "bg-black text-white px-6 py-3 rounded-full shadow-2xl flex items-center gap-3 transform transition-all duration-500 hover:scale-105 pointer-events-auto backdrop-blur-md bg-opacity-90 border border-gray-800";

        // Initial State (Fly in from top)
        toast.className = `${baseClasses} -translate-y-20 opacity-0`;

        const icon = type === 'success'
            ? '<div class="w-6 h-6 bg-green-500 rounded-full flex items-center justify-center flex-shrink-0"><svg class="w-4 h-4 text-black" fill="none" stroke="currentColor" viewBox="0 0 24 24" stroke-width="3"><path stroke-linecap="round" stroke-linejoin="round" d="M5 13l4 4L19 7"></path></svg></div>'
            : '<div class="w-6 h-6 bg-red-500 rounded-full flex items-center justify-center flex-shrink-0"><svg class="w-4 h-4 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24" stroke-width="3"><path stroke-linecap="round" stroke-linejoin="round" d="M6 18L18 6M6 6l12 12"></path></svg></div>';

        toast.innerHTML = `${icon} <span class="font-medium text-sm antialiased">${message}</span>`;

        container.appendChild(toast);

        // Animate in
        requestAnimationFrame(() => {
            toast.classList.remove('-translate-y-20', 'opacity-0');
        });

        // Remove after 3 seconds
        setTimeout(() => {
            toast.classList.add('-translate-y-20', 'opacity-0');
            setTimeout(() => {
                toast.remove();
            }, 500);
        }, 4000);
    }
</script>

